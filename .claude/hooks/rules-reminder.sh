#!/bin/bash
# rules-reminder.sh
# UserPromptSubmit hook: нагадує Claude про ключові правила
# Спрацьовує ТІЛЬКИ коли Роман скаржиться на якість відповідей (сигнали болю).
#
# Історія:
#  - 29.04 m4Q1o: запускався кожне 5-те повідомлення + сигнали болю
#  - 01.05 6ANWm: прибрано «кожне 5-те» — це шум коли все нормально.
#    Залишено лише сигнали болю Романа («простіше / коротше / не розумію» тощо).
#    Принцип: нагадування — реакція на проблему, не профілактика.

# JSON input через stdin
input=$(cat)

# Витягаємо prompt юзера (через python3 якщо є, інакше grep)
prompt=""
if command -v python3 >/dev/null 2>&1; then
  prompt=$(echo "$input" | python3 -c "import sys,json; print(json.load(sys.stdin).get('prompt',''))" 2>/dev/null)
fi
if [[ -z "$prompt" ]]; then
  # fallback: простий grep (не завжди надійний для складного JSON)
  prompt=$(echo "$input" | grep -oE '"prompt"[[:space:]]*:[[:space:]]*"[^"]*"' | head -1 | sed 's/.*"prompt"[[:space:]]*:[[:space:]]*"//;s/"$//')
fi

# Тригер-слова (індикатори що Клод поламав правило)
trigger_match=""
prompt_lower=$(echo "$prompt" | tr '[:upper:]' '[:lower:]')
for word in "простіше" "коротше" "не розумію" "довго" "технічно" "простирадло" "поясни як людині" "кілометров" "не читаю" "скороти"; do
  if [[ "$prompt_lower" == *"$word"* ]]; then
    trigger_match="$word"
    break
  fi
done

# Спрацьовуємо ТІЛЬКИ якщо знайдено тригер болю
if [[ -n "$trigger_match" ]]; then
  cat <<EOF
📋 СИГНАЛ БОЛЮ РОМАНА: «$trigger_match» → попередня відповідь була перетягнута/заплутана.

ЯК РЕАГУВАТИ:
  1. Визнати коротко («так, перетягнув»)
  2. БЕЗ преамбули («Зрозумів / Прийнято / Записав»)
  3. Переписати простіше + коротше:
     • UI — описуй ЩО ЮЗЕР БАЧИТЬ (не назви функцій/класів/HEX)
     • Розмір — 5-15 рядків. Без повторів і резюме
     • Терміни-коди — пояснюй ОДИН раз у дужках, не по 5
  4. Без виправдань

CLAUDE.md секція «📏 Розмір відповідей» — джерело правди.
EOF
fi
